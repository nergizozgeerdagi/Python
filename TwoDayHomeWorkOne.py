#Üçgen alanı: h.a/2
#a = üçgenin tabanı
#h = üçgenin yüksekliği 
# Taban çarpı yükseklik bölü iki
ucgenİsmi = input("Üçgeni isimlendiriniz: ")
tabanUzunluk = float(input("Üçgenin taban uzunluğunu giriniz: "))
yukseklikUzunluk = float(input("Üçgenin yükseklik uzunluğunu giriniz: "))
ucgenAlani = (tabanUzunluk*yukseklikUzunluk)/2
print(ucgenİsmi , "dik üçgeninin alanı: " , ucgenAlani)